CREATE TABLE Settings (
  "Id" TEXT,
  "Value" TEXT,
  PRIMARY KEY("Id")
);

CREATE TABLE Users (
	"Name"	TEXT NOT NULL,
	"Pass"	TEXT NOT NULL,
	"Motto"	TEXT DEFAULT "",
	"Figure"	TEXT,
	"PoolFigure"	TEXT DEFAULT "ch=s02/53,51,44",
	"Sex"	TEXT,
	"Mail"	TEXT,
	"Birthday"	TEXT,
	PRIMARY KEY("Name")
);

CREATE TABLE Purse (
	"User"	TEXT NOT NULL,
	"Credits"	NUMERIC DEFAULT 0.0,
	"Tickets"	INTEGER DEFAULT 0,
	"CamRoll"	INTEGER DEFAULT 0,
	PRIMARY KEY("User"),
	FOREIGN KEY("User") REFERENCES "Users"("Name")
);

CREATE TABLE Room (
	"Id"	INTEGER PRIMARY KEY AUTOINCREMENT,
	"User"	TEXT NOT NULL,
	"Floor"	TEXT,
	"Name"	TEXT,
	"Model"	TEXT,
	"Door"	TEXT,
	"ShowOwner"	INTEGER,
	FOREIGN KEY("User") REFERENCES Users("Name")
);

